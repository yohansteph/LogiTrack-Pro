    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Driver driver = new Driver();
        driver.setId(Integer.parseInt(txtId.getText()));
        driver.setName(txtName.getText());
        driver.setLicenseNumber(txtLicenseNumber.getText());
        driver.setContact(txtContact.getText());
        driver.setStatus(cmbStatus.getSelectedItem().toString());
        driver.setEmail(txtDriverEmail.getText());

        boolean success = driverService.addDriver(driver);
        if (success) {
            JOptionPane.showMessageDialog(this, "Driver added successfully");
            loadDriversToTable();
            clearFields();
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add driver");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnAddActionPerformed

