private void loadDriversToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing rows

        List<Driver> drivers = driverService.getAllDrivers();
        for (Driver d : drivers) {
            model.addRow(new Object[]{
                d.getId(),
                d.getName(),
                d.getLicenseNumber(),
                d.getContact(),
                d.getStatus(),
                d.getEmail()
            });
        }
    }

    private void clearFields() {
        txtId.setText("");
        txtName.setText("");
        txtLicenseNumber.setText("");
        txtContact.setText("");
        cmbStatus.setSelectedIndex(0);
    }

    private void addTableSelectionListener() {
        jTable1.getSelectionModel().addListSelectionListener(event -> {
            if (!event.getValueIsAdjusting() && jTable1.getSelectedRow() != -1) {
                int selectedRow = jTable1.getSelectedRow();
                txtId.setText(tableModel.getValueAt(selectedRow, 0).toString());
                txtName.setText(tableModel.getValueAt(selectedRow, 1).toString());
                txtLicenseNumber.setText(tableModel.getValueAt(selectedRow, 2).toString());
                txtContact.setText(tableModel.getValueAt(selectedRow, 3).toString());
                cmbStatus.setSelectedItem(tableModel.getValueAt(selectedRow, 4).toString()); 
                txtDriverEmail.setText(tableModel.getValueAt(selectedRow, 5).toString());
            }
        });
    }

